package huit.fit.lv_lab4;

public class QG {
    int imgAnh;
    String tenQG;

    public QG(int imgAnh, String tenQG) {
        this.imgAnh = imgAnh;
        this.tenQG = tenQG;
    }

    public QG() {
    }

    public int getImgAnh() {
        return imgAnh;
    }

    public void setImgAnh(int imgAnh) {
        this.imgAnh = imgAnh;
    }

    public String getTenQG() {
        return tenQG;
    }

    public void setTenQG(String tenQG) {
        this.tenQG = tenQG;
    }
}
